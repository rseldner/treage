/* ── Tree walk: find path from root to a given node ID ── */
function findPathToNode(targetId, node, pathSoFar) {
  const current = pathSoFar.concat(node);
  if (node.id === targetId) return current;
  if (!node.children) return null;
  for (const child of node.children) {
    const result = findPathToNode(targetId, child, current);
    if (result) return result;
  }
  return null;
}

/* ── Tree walk: jump directly to any node by clicking it ── */
function treeWalkJumpTo(targetData, layoutNode) {
  const fullPath = findPathToNode(targetData.id, TREE, []);
  if (!fullPath || fullPath.length === 0) return;

  // fullPath[0] is TREE (start), fullPath[1] is first question, etc.
  // iState.path starts from the first question (not start node).
  // choices[i] is the decision taken FROM path[i].
  const newPath    = [];
  const newChoices = [];

  for (let i = 1; i < fullPath.length; i++) {
    newPath.push(fullPath[i]);
    if (i < fullPath.length - 1) {
      newChoices.push({
        label: fullPath[i + 1].edgeLabel || fullPath[i + 1].type,
        node:  fullPath[i + 1],
      });
    }
  }

  iState.path    = newPath;
  iState.choices = newChoices;
  highlightActivePath();
  panToNode(layoutNode);
  wRender();
  iRender();
}


function wCountDescendants(node) {
  if (!node.children || node.children.length === 0) return 0;
  return node.children.reduce((sum, c) => sum + 1 + wCountDescendants(c), 0);
}

function wEdgeClass(label) {
  if (label === 'YES') return 'tg-yes';
  if (label === 'NO')  return 'tg-no';
  return 'tg-def';
}

