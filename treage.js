/* ── Layout ── */
function autoSize(nodeData) {
  if (nodeData.type === 'start') {
    // uppercase + letter-spacing:2px makes chars render ~13px wide, plus generous padding
    const tw = Math.max(160, (nodeData.title || 'Start').length * 13 + 48);
    return { w: Math.min(tw, LAYOUT.maxWidth), h: 46 };
  }
  const title = nodeData.title || '', hint = nodeData.hint || '';
  const tl = Math.ceil(title.length / LAYOUT.charsPerLine) || 1;
  const hl = hint ? Math.ceil(hint.length  / LAYOUT.charsPerLine) || 1 : 0;
  const contentH = 20 + (tl * LAYOUT.lineHeight) + (hl * (LAYOUT.lineHeight - 2)) + (hl > 0 ? 8 : 0);
  return { w: LAYOUT.maxWidth, h: Math.max(LAYOUT.minHeight, contentH + LAYOUT.paddingV) };
}

function buildLayout(data) {
  const root = d3.hierarchy(data);
  root.each(d => { const s = autoSize(d.data); d.nodeW = s.w; d.nodeH = s.h; });
  d3.tree().nodeSize([1,1])
    .separation((a,b) => (a.nodeW/2) + (b.nodeW/2) + LAYOUT.nodeHGap)(root);
  const depthMaxH = {};
  root.each(d => { depthMaxH[d.depth] = Math.max(depthMaxH[d.depth] || 0, d.nodeH); });
  const depthY = {}; let cumY = 0;
  const maxDepth = Math.max(...Object.keys(depthMaxH).map(Number));
  for (let i = 0; i <= maxDepth; i++) { depthY[i] = cumY; cumY += (depthMaxH[i] || 72) + LAYOUT.nodeVGap; }
  root.each(d => { d.px = d.x; d.py = depthY[d.depth]; });
  return { nodes: root.descendants(), links: root.links() };
}

/* ── Tree walk: path highlighting ── */
function activePathIds() {
  // Returns a Set of node IDs that are on the current walk path.
  // iState.path holds node data objects; collect their IDs.
  const ids = new Set();
  ids.add(TREE.id); // start node always active
  iState.path.forEach(n => ids.add(n.id));
  return ids;
}

function highlightActivePath() {
  if (!treeG || !treeLayout) return;
  const pathIds = activePathIds();
  const hasWalk = iState.choices.length > 0;

  // Edges: active path = full opacity + thicker; others dim
  treeG.selectAll('.tg-walk-edge').each(function(d) {
    const onPath = pathIds.has(d.target.data.id) && pathIds.has(d.source.data.id);
    d3.select(this)
      .attr('stroke-opacity', hasWalk ? (onPath ? 1.0 : 0.12) : 0.65)
      .attr('stroke-width',   onPath  ? 2.5 : 1.5);
  });

  // Edge label hit areas + text: dim off-path
  treeG.selectAll('.tg-walk-label-group').each(function(d) {
    const onPath = pathIds.has(d.target.data.id) && pathIds.has(d.source.data.id);
    d3.select(this).attr('opacity', hasWalk ? (onPath ? 1.0 : 0.15) : 1.0);
  });

  // Nodes: active path = full; others dim
  treeG.selectAll('.tg-tree-node').each(function(d) {
    const onPath = pathIds.has(d.data.id);
    d3.select(this).attr('opacity', hasWalk ? (onPath ? 1.0 : 0.18) : 1.0);
  });
}

/* ── Tree walk: pan to a layout node ── */
function panToNode(d, duration) {
  if (!treeSvg || !treeZoom || !container) return;
  const dur = duration !== undefined ? duration : 450;
  const t = d3.zoomTransform(treeSvg.node());
  const targetX = d.px;
  const targetY = d.py + d.nodeH / 2;
  treeSvg.transition().duration(dur)
    .call(treeZoom.translateTo, targetX, targetY);
}

/* ── Tree walk: advance from an edge click ── */
function treeWalkAdvance(targetNode, layoutNode) {
  // Push onto shared iState
  iState.choices.push({ label: targetNode.edgeLabel || targetNode.type, node: targetNode });
  iState.path.push(targetNode);
  highlightActivePath();
  panToNode(layoutNode);
}

/* ── Tree walk: reset ── */
function treeWalkReset() {
  iState.path    = [];
  iState.choices = [];
  highlightActivePath();
  if (treeLayout) {
    const startNode = treeLayout.nodes.find(n => n.data.id === TREE.id);
    if (startNode) panToNode(startNode, 400);
  }
  iReset();
}

